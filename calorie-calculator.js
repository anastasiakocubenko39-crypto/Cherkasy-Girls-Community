window.calcBJU = function() {
  const raw    = (document.getElementById("calcProduct")?.value || "").trim().toLowerCase();
  const amount = parseFloat(document.getElementById("calcAmount")?.value) || 100;
  const result = document.getElementById("calcResult");
  if (!result) return;

  // Точний збіг → частковий збіг
  let item = NUTRITION_DB[raw];
  let foundKey = raw;
  if (!item) {
    const partialKey = Object.keys(NUTRITION_DB).find(k => k.includes(raw) || raw.includes(k));
    if (partialKey) { item = NUTRITION_DB[partialKey]; foundKey = partialKey; }
  }

  if (!item) {
    result.className = "calc-result error";
    result.innerHTML = `<span>❌ «<b>${raw}</b>» не знайдено.</span>
      <br><small style="display:block;margin-top:8px;opacity:.7">
      Спробуй: ${Object.keys(NUTRITION_DB).slice(0,7).join(", ")}...
      </small>`;
    return;
  }

  const k   = amount / 100;
  const p   = (item.p * k).toFixed(1);
  const f   = (item.f * k).toFixed(1);
  const c   = (item.c * k).toFixed(1);
  const cal = Math.round(item.cal * k);

  const displayName = foundKey.charAt(0).toUpperCase() + foundKey.slice(1);

  result.className = "calc-result show";
  result.innerHTML = `
    <div class="calc-product-name">${displayName} · ${amount}г</div>
    <div class="calc-macros">
      <div class="macro-card prot"><div class="macro-val">${p}г</div><div class="macro-lbl">Білки</div></div>
      <div class="macro-card fat"><div class="macro-val">${f}г</div><div class="macro-lbl">Жири</div></div>
      <div class="macro-card carb"><div class="macro-val">${c}г</div><div class="macro-lbl">Вуглеводи</div></div>
      <div class="macro-card kcal"><div class="macro-val">${cal}</div><div class="macro-lbl">ккал</div></div>
    </div>`;

  // Додаємо в історію
  const hist = document.getElementById("calcHistory");
  if (!hist) return;
  if (!hist.querySelector(".calc-history-title")) {
    const title = document.createElement("div");
    title.className = "calc-history-title";
    title.textContent = "Останні розрахунки:";
    hist.prepend(title);
  }
  const row = document.createElement("div");
  row.className = "hist-row";
  row.innerHTML = `<span>${displayName} · ${amount}г</span><span>Б:${p}г &nbsp;Ж:${f}г &nbsp;В:${c}г &nbsp;<b>${cal} ккал</b></span>`;
  const titleEl = hist.querySelector(".calc-history-title");
  hist.insertBefore(row, titleEl ? titleEl.nextSibling : hist.firstChild);
  const rows = hist.querySelectorAll(".hist-row");
  if (rows.length > 6) rows[rows.length - 1].remove();
};

